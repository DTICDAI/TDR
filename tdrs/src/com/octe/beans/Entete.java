package com.octe.beans;

public class Entete {

	private String entete;
	private Integer id_entete;
	private String etat;
	private String logo;
	private String entete_droite;
	
	
	
	public String getLogo() {
		return logo;
	}
	public void setLogo(String logo) {
		this.logo = logo;
	}
	public String getEntete_droite() {
		return entete_droite;
	}
	public void setEntete_droite(String entete_droite) {
		this.entete_droite = entete_droite;
	}
	public String getEntete() {
		return entete;
	}
	public void setEntete(String entete) {
		this.entete = entete;
	}
	public Integer getId_entete() {
		return id_entete;
	}
	public void setId_entete(Integer id_entete) {
		this.id_entete = id_entete;
	}
	public String getEtat() {
		return etat;
	}
	public void setEtat(String etat) {
		this.etat = etat;
	}
	
	
	
		
}
